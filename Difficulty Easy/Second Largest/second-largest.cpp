class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        
        set<int>mp(arr.begin(),arr.end());
        if (mp.size()<2){
            return -1;
        }
        priority_queue<int>mpp;
        
         for (auto i:mp){
            mpp.push(i);
        }
        
        mpp.pop();
        int n=mpp.top();
        
        return n;
        
        
        
    }
};

